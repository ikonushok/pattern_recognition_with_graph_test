def get_positional_encoding(seq_len, d_model):
    position = torch.arange(seq_len).unsqueeze(1)
    div_term = torch.exp(torch.arange(0, d_model, 2).float() * (-math.log(10000.0) / d_model))
    pos_encoding = torch.zeros(seq_len, d_model)
    pos_encoding[:, 0::2] = torch.sin(position * div_term)
    pos_encoding[:, 1::2] = torch.cos(position * div_term)
    return pos_encoding


def normalize_edge_weights(edge_weights):
    min_val = torch.min(edge_weights)
    max_val = torch.max(edge_weights)
    normalized_weights = (edge_weights - min_val) / (max_val - min_val)
    return normalized_weights


def log_diff(x1, x2):
    return torch.log(x2 / x1)


def create_single_observation(observation):
    x_tensor = torch.tensor(observation, dtype=torch.float)

    num_rows, num_cols = x_tensor.shape
    lag_size = 10

    edge_indices = {}
    edge_weights = {}

    for col in range(num_cols):
        edge_indices[col] = []
        edge_weights[col] = []

    for col in range(num_cols):
        for i in range(num_rows):
            for j in range(max(0, i - lag_size), min(num_rows, i + lag_size + 1)):
                if i != j:
                    weight = log_diff(x_tensor[i, col], x_tensor[j, col])
                    edge_indices[col].append([i, j])
                    edge_weights[col].append(weight)

    for col in range(num_cols):
        edge_weights[col] = torch.tensor(edge_weights[col], dtype=torch.float)
        edge_weights[col] = normalize_edge_weights(edge_weights[col])  # Normalize weights

    for col in range(num_cols):
        edge_indices[col] = torch.tensor(edge_indices[col], dtype=torch.long).t().contiguous()

    positional_encodings = get_positional_encoding(60, 10)

    data = Data(x=positional_encodings)
    for col in range(num_cols):
        setattr(data, f'edge_index_type_{col}', edge_indices[col])
        setattr(data, f'edge_attr_type_{col}', edge_weights[col])
    return data


def transform_data_to_graphs(filenames: List[List[str]]) -> (List[Data], List[torch.Tensor]):
    ds, ds_labels = [], []
    for i, (x_name, y_name) in tqdm(enumerate(filenames)):
        x = np.load(x_name)
        y = np.load(y_name)
        for observation, label in zip(x, y):
            ds.append(create_single_observation(observation))
            ds_labels.append(torch.tensor(label + i * 2))
    return ds, ds_labels


class GraphDataset(torch.utils.data.Dataset):
    def __init__(self, dataset):
        self.graph_data, self.labels = dataset

    def __len__(self):
        return len(self.graph_data)

    def __getitem__(self, idx):
        return self.graph_data[idx], self.labels[idx]


class SiameseMNIST(Dataset):
    """
    Train: For each sample creates randomly a positive or a negative pair
    Test: Creates fixed pairs for testing
    """

    def __init__(self, mnist_dataset):
        self.mnist_dataset = mnist_dataset

        self.train = self.mnist_dataset.train
        self.transform = self.mnist_dataset.transform

        if self.train:
            self.train_labels = self.mnist_dataset.train_labels
            self.train_data = self.mnist_dataset.train_data
            self.labels_set = set(self.train_labels.numpy())
            self.label_to_indices = {label: np.where(self.train_labels.numpy() == label)[0]
                                     for label in self.labels_set}
        else:
            # generate fixed pairs for testing
            self.test_labels = self.mnist_dataset.test_labels
            self.test_data = self.mnist_dataset.test_data
            self.labels_set = set(self.test_labels.numpy())
            self.label_to_indices = {label: np.where(self.test_labels.numpy() == label)[0]
                                     for label in self.labels_set}

            random_state = np.random.RandomState(29)

            positive_pairs = [[i,
                               random_state.choice(self.label_to_indices[self.test_labels[i].item()]),
                               1]
                              for i in range(0, len(self.test_data), 2)]

            negative_pairs = [[i,
                               random_state.choice(self.label_to_indices[
                                                       np.random.choice(
                                                           list(self.labels_set - set([self.test_labels[i].item()]))
                                                       )
                                                   ]),
                               0]
                              for i in range(1, len(self.test_data), 2)]
            self.test_pairs = positive_pairs + negative_pairs

    def __getitem__(self, index):
        if self.train:
            target = np.random.randint(0, 2)
            img1, label1 = self.train_data[index], self.train_labels[index].item()
            if target == 1:
                siamese_index = index
                while siamese_index == index:
                    siamese_index = np.random.choice(self.label_to_indices[label1])
            else:
                siamese_label = np.random.choice(list(self.labels_set - set([label1])))
                siamese_index = np.random.choice(self.label_to_indices[siamese_label])
            img2 = self.train_data[siamese_index]
        else:
            img1 = self.test_data[self.test_pairs[index][0]]
            img2 = self.test_data[self.test_pairs[index][1]]
            target = self.test_pairs[index][2]

        img1 = Image.fromarray(img1.numpy(), mode='L')
        img2 = Image.fromarray(img2.numpy(), mode='L')
        if self.transform is not None:
            img1 = self.transform(img1)
            img2 = self.transform(img2)
        return (img1, img2), target

    def __len__(self):
        return len(self.mnist_dataset)


